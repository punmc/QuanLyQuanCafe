    private void loadTableInfo() {
        lstNV = NhanVienDAO.layDSNhanVien();
        
        DefaultTableModel dtm = new DefaultTableModel();
        dtm = (DefaultTableModel)tblDsNhanVien.getModel();
        
        dtm.setNumRows(0);
        for(NhanVien nv : lstNV){
            Vector<Object> nhanvien = new Vector<>();
            TaiKhoan tk = new TaiKhoan();
            tk = TaiKhoanDAO.layTaiKhoan_TheoMaNV(nv.getMaNV());
            
            nhanvien.add(nv.getMaNV());
            nhanvien.add(tk.getTenDN());
            nhanvien.add(nv.getTenNV());
            nhanvien.add(nv.getNgaySinh());
            nhanvien.add(nv.getGioiTinh());
            
            nhanvien.add(nv.getSDT());
            nhanvien.add(nv.getDiaChi());
            nhanvien.add(nv.getChucVu());
            
            dtm.addRow(nhanvien);
        }
    }
    
