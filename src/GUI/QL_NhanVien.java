    //Thêm NV
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
       if(NhanVienDAO.ktraNVCoTonTai(txtMaNV.getText())== false){
            try {
                NhanVien nv = new NhanVien(txtHoTen.getText(), txtNgaySinh.getText(), txtSDT.getText(), txtChucVu.getText(), txtDiaChi.getText(), txtGioiTinh.getText());
                TaiKhoan tk = new TaiKhoan(txtTenDN.getText(), txtMatKhau1.getText());
                boolean result = false;
                if(NhanVienDAO.ktraNV_TrungTenDN(txtTenDN.getText())){
                    JOptionPane.showConfirmDialog(null, "Tên đăng nhập không được trùng", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                
                }else{
                    result = NhanVienDAO.themNhanVien(nv,tk);
                }
                
                if(result == false){
                    JOptionPane.showConfirmDialog(null, "Thêm thất bại!!", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                }
                else{
                    loadTableInfo();
                    JOptionPane.showConfirmDialog(null, "Thêm thành công!","Thông báo",JOptionPane.OK_OPTION);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        else{
            int option = JOptionPane.showConfirmDialog(null, "Mã nv nhập bị trùng, bạn có muốn chỉnh sửa thông tin nv không?", "Thông báo", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                try {
                    NhanVien nv = new NhanVien(txtMaNV.getText(),txtHoTen.getText(), txtNgaySinh.getText(), txtSDT.getText(), txtChucVu.getText(), txtDiaChi.getText(), txtGioiTinh.getText());
                    boolean result = NhanVienDAO.capNhatThongTin_NhanVien(nv);
                    
                    if(result == false)
                        JOptionPane.showConfirmDialog(null, "Cập nhật thất bại!!", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                    else{
                        loadTableInfo();
                        JOptionPane.showConfirmDialog(null, "Thêm thành công!","Thông báo",JOptionPane.OK_OPTION);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }

    }//GEN-LAST:event_btnThemActionPerformed

