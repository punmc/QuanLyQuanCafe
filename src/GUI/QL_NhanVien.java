    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        boolean ktraTrung_MaNV = NhanVienDAO.ktraNVCoTonTai(txtMaNV.getText());
        
        NhanVien nv = new NhanVien();
        TaiKhoan tk;
        if(ktraTrung_MaNV == true){
            nv.setMaNV(txtMaNV.getText());
            nv.setTenNV(txtHoTen.getText());
            nv.setChucVu(txtChucVu.getText());
            nv.setDiaChi(txtDiaChi.getText());
            nv.setGioiTinh(txtGioiTinh.getText());
            nv.setNgaySinh(txtNgaySinh.getText());
            nv.setSDT(txtSDT.getText());
            
            boolean kq = NhanVienDAO.capNhatThongTin_NhanVien(nv);
            if(kq){
                JOptionPane.showConfirmDialog(null, String.format("Cập nhật thông tin nhân viên có mã %s thành công!!!!",txtMaNV.getText()),"Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                loadTableInfo();
            }
            else{
                JOptionPane.showConfirmDialog(null, String.format("Cập nhật thông tin nhân viên có mã %s không thành công!!!!",txtMaNV.getText()),"Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
            }
        }
        else{
            int temp = JOptionPane.showConfirmDialog(null, "Mã nhân viên không tồn tại trong danh sách, bạn có muốn thêm mới nhân viên không?","Thông báo", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_OPTION);
            tk = new TaiKhoan(txtTenDN.getText(), txtMatKhau1.getText());
            if(temp == JOptionPane.YES_OPTION){
                nv.setMaNV(txtMaNV.getText());
                nv.setTenNV(txtHoTen.getText());
                nv.setChucVu(txtChucVu.getText());
                nv.setDiaChi(txtDiaChi.getText());
                nv.setGioiTinh(txtGioiTinh.getText());
                nv.setNgaySinh(txtNgaySinh.getText());
                nv.setSDT(txtSDT.getText());
                boolean kq = NhanVienDAO.themNhanVien(nv, tk);
                if(kq){
                    JOptionPane.showConfirmDialog(null, "Thêm nhân viên thành công!","Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                    loadTableInfo();
                }
                else{
                    JOptionPane.showConfirmDialog(null, "Thêm nhân viên không thành công!","Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                }
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

