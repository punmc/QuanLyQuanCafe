    private void loadTableInfo() {

        lstKH = KhachHangDAO.layDSKhachHang();
        
        DefaultTableModel dtm = new DefaultTableModel();
        dtm = (DefaultTableModel)tblKhachHang.getModel();
        
                
        dtm.setNumRows(0);
        for(KhachHang kh : lstKH){
            Vector<Object> khachhang = new Vector<>();
            float tyLeGiamGia = LoaiKhachHangDAO.layTyLeGiamGia(kh.getMaLoaiKH());
            
            khachhang.add(kh.getMaKH());
            khachhang.add(kh.getTenKH());
            khachhang.add(kh.getSDT());
            khachhang.add(kh.getDiaChi());
            khachhang.add(kh.getDiemTL());
            khachhang.add(kh.getMaLoaiKH());
            khachhang.add(tyLeGiamGia);
            
            dtm.addRow(khachhang);
        }
        
        
    }
     
