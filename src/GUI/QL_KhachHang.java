    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
         if(KhachHangDAO.ktraKHCoTonTai(txtMaKH.getText())== false){
            try {
                KhachHang kh = new KhachHang();
                kh.setMaKH(txtMaKH.getText());
                kh.setTenKH(txtHoTen.getText());
                kh.setSDT(txtSDT.getText());
                kh.setDiaChi(txtDiaChi.getText());
                kh.setMaLoaiKH(txtMaLoaiKH.getText());
            
                
                boolean result = false;
                if(KhachHangDAO.ktraSDT_KhachHang(txtSDT.getText())){
                    JOptionPane.showConfirmDialog(null, "SDT không được trùng", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                
                }else{
                    result = KhachHangDAO.themKhachHang(kh);
                }
                
                if(result == false){
                    JOptionPane.showConfirmDialog(null, "Thêm thất bại!!", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                }
                else{
                    loadTableInfo();
                    JOptionPane.showConfirmDialog(null, "Thêm thành công!","Thông báo",JOptionPane.OK_OPTION);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        else{
            int option = JOptionPane.showConfirmDialog(null, "Mã kh nhập bị trùng, bạn có muốn chỉnh sửa thông tin nv không?", "Thông báo", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                try {
                    
                    KhachHang kh = new KhachHang();
  //                  KhachHang kh = new KhachHang(txtMaKH.getText(),txtHoTen.getText(), txtSDT.getText(), txtDiaChi.getText(), txtMaKH1.getText());
                   kh.setMaKH(txtMaKH.getText());
                   kh.setTenKH(txtHoTen.getText());
                   kh.setSDT(txtSDT.getText());
                   kh.setDiaChi(txtDiaChi.getText());
                   kh.setMaLoaiKH(txtMaLoaiKH.getText());
                    boolean result = KhachHangDAO.capNhatThongTin_KhachHang(kh);
                    
                    if(result == false)
                        JOptionPane.showConfirmDialog(null, "Cập nhật thất bại!!", "Thông báo", JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                    else{
                        loadTableInfo();
                        JOptionPane.showConfirmDialog(null, "Thêm thành công!","Thông báo",JOptionPane.OK_OPTION);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }



    }//GEN-LAST:event_btnThemActionPerformed

