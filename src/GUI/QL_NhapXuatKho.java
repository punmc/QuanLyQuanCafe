    private void loadCombobox() {
        lstNguyenLieu = NguyenLieuDAO.layDSNguyenLieu();
        lstNhaCC = NhaCungCapDAO.layDSNhaCC();
        
        for (NguyenLieu nl : lstNguyenLieu){
            cboMaNguyenLieu.addItem(nl.getMaNL());
        }
        
        for (NhaCungCap ncc : lstNhaCC){
            cboMaNhaCC.addItem(ncc.getMaNCC());
        }
        
        for (NguyenLieu nl : lstNguyenLieu){
            cboTenNguyenLieu.addItem(nl.getTenNL());
        }
    }

