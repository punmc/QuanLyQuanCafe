    private void btnThemMatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMatHangActionPerformed
        // TODO add your handling code here:
        if (txtGiaNhap.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập giá nhập!");
                txtGiaNhap.setFocusable(true);
        }
            
        if ((int)txtSoLuong1.getValue() == 0){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập số lượng nhập!");
            txtSoLuong1.setFocusable(true);
        }
        
        int row = tblThongTinNhap.getSelectedRow();
        if (row != -1){
            String manl = cboMaNguyenLieu.getSelectedItem().toString();
            ChiTietPhieuNhap ct = new ChiTietPhieuNhap(tblThongTinNhap.getValueAt(row, 0).toString(), manl, NguyenLieuDAO.layTenNguyenLieu(manl), Integer.parseInt(txtSoLuong1.getValue().toString()), BigDecimal.valueOf(Double.parseDouble(txtGiaNhap.getText())),BigDecimal.ZERO);
            boolean temp = ChiTietPhieuNhapDAO.themMoiCTPN(ct);
            if(temp == true){
                JOptionPane.showMessageDialog(null, "Thêm thành công chi tiết phiếu nhập");
                loadTableInfo_NhapKho();
                loadCTPN(tblThongTinNhap.getValueAt(row, 0).toString());
                loadTableInfo_NguyenLieu();
            }
            else{
                JOptionPane.showMessageDialog(null, "Thêm thất bại chi tiết phiếu nhập");
            }
        }
        else{
            int x = JOptionPane.showConfirmDialog(null, "Bạn chưa chọn phiếu nhập muốn nhập nguyên liệu, bạn có muốn thêm mới phiếu nhập không", "Thông báo", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION);
            if(x == JOptionPane.YES_OPTION){
                PhieuNhap pn = new PhieuNhap(cboMaNhaCC.getSelectedItem().toString(), Run.tk.getMaNV(), txtGhiChu1.getText());
                boolean temp = PhieuNhapDAO.themMoiPhieuNhap(pn);
                if(temp == true){
                    JOptionPane.showMessageDialog(null, "Thêm mới phiếu nhập thành công");
                    loadTableInfo_NhapKho();
                }
            }
        }
        
    }//GEN-LAST:event_btnThemMatHangActionPerformed

