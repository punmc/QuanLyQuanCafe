    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:

        boolean ktraTrung_MaNL = NguyenLieuDAO.ktraNguyenLieuCoTonTai(txtMaNL.getText());

        NguyenLieu nguyenlieu = new NguyenLieu();
        if(ktraTrung_MaNL == true){
            nguyenlieu.setMaNL(txtMaNL.getText());
            nguyenlieu.setTenNL(txtTenNL.getText());
            nguyenlieu.setDVT(txtDVT.getText());
            nguyenlieu.setSoLuong(Integer.parseInt(txtSoLuong.getText()));

            boolean kq = NguyenLieuDAO.capNhatThongTin_NguyenLieu(nguyenlieu);
            if(kq){
                JOptionPane.showConfirmDialog(null, String.format("Cập nhật thông tin Nguyên Liệu có mã %s thành công!!!!",txtMaNL.getText()),"Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                loadTableInfo_NguyenLieu();
            }
            else{
                JOptionPane.showConfirmDialog(null, String.format("Cập nhật thông tin Nguyên Liệu có mã %s không thành công!!!!",txtMaNL.getText()),"Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
            }
        }
        else{
            int temp = JOptionPane.showConfirmDialog(null, "Mã Nguyên Liệu không tồn tại trong danh sách, bạn có muốn thêm mới Nguyên Liệu không?","Thông báo", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_OPTION);
            if(temp == JOptionPane.YES_OPTION){
                nguyenlieu.setMaNL(txtMaNL.getText());
                nguyenlieu.setTenNL(txtTenNL.getText());
                nguyenlieu.setDVT(txtDVT.getText());
                nguyenlieu.setSoLuong(Integer.parseInt(txtSoLuong.getText()));

                boolean kq = NguyenLieuDAO.themNguyenLieu(nguyenlieu);
                if(kq){
                    JOptionPane.showConfirmDialog(null, "Thêm Nguyên Liệu thành công!","Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                    loadTableInfo_NguyenLieu();
                }
                else{
                    JOptionPane.showConfirmDialog(null, "Thêm Nguyên Liệu không thành công!","Thông báo",JOptionPane.INFORMATION_MESSAGE,JOptionPane.OK_OPTION);
                }
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

